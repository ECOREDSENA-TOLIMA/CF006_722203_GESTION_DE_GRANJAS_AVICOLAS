      h1 Programa de bioseguridad para granjas en avícola

    p Un programa de bioseguridad según Fenavi (2014), es el “sistema que protege la salud de las aves, mediante la implementación de una serie de medidas de higiene, sanidad y control ambiental, destinadas a disminuir la exposición a agentes infectocontagiosos que puedan afectar la explotación avícola” (p.116).

    p Por otro lado, la Resolución 3651, (2014) es el: “conjunto de medidas, acciones y procedimientos que se deben tomar para evaluar, evitar, prevenir, mitigar, manejar y/o controlar los posibles riesgos sanitarios y sus efectos directos o indirectos en la salud humana, el medio ambiente, la biodiversidad, la productividad y producción agropecuaria.” (p. 6) las cuales se deben implementar para prevenir enfermedades que diezman la producción, generando pérdidas económicas. 

    p Como puede observar los programas de bioseguridad en las granjas avícolas bioseguras tienen como finalidad según FENAVI (2019) “prevenir, controlar y erradicar la presencia de enfermedades aviares” (p.4); y deben cumplir según (ICA, 2014) con “procedimientos operativos estandarizados (POE) y tener una capacidad instalada que permite alojar un número igual o superior a doscientas (200) aves de la misma especie y tipo de explotación” (p.2). 

    p.mb-5 Se encarga también de controlar la sanidad y la producción eficiente de las aves, por lo que, se tienen en cuenta aspectos higiénico sanitarios y ambientales para prevenir de agentes causales que afecten la productividad y disminuyen los beneficios económicos. 

